/**
 * Math.ceil functions in the javascript used to roundup the nummber that is nearest to the integer
 * 
 * 
 */
console.log(Math.ceil(1.4)); 
//2
console.log(Math.ceil(1.6)); 
//2
console.log(Math.round(-1.4));
//-1
console.log(Math.round(-1.6));
//-1

/**
 * Math.floor returns the largest or equal integer that is less than the given value.
 */
console.log(Math.floor(1.4));
// 1
console.log(Math.floor(1.6));
// 1
console.log(Math.floor(-1.4));
// -2
console.log(Math.floor(-1.6));
// -2